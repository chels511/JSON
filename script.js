   function displayGame() {
    const gameContainer = document.body;

    gameData.game.forEach(project => {
      const projectElement = document.createElement('div');
      projectElement.classList.add('project');
      projectElement.style.color = project.fontColor;
      projectElement.style.backgroundColor = project.backgroundColor;

      const projectName = document.createElement('h2');
      projectName.textContent = `${project.name} (${project.year})`;

      const projectDescription = document.createElement('p');
      projectDescription.textContent = project.description;

      const projectImages = project.images.map(image => ({
        src: image,
        opts: { caption: project.description }
      }));

      $(projectElement).on('click', function() {
        $.fancybox.open(projectImages, {
          loop: true, 
          thumbs: {
            autoStart: true, 
          },
        });
      });

      projectElement.appendChild(projectName);
      projectElement.appendChild(projectDescription);

      const imageContainer = document.createElement('div');
      imageContainer.classList.add('image-container');

      projectImages.forEach(image => {
        const thumbnail = document.createElement('img');
        thumbnail.src = image.src;
        thumbnail.alt = project.name;
        thumbnail.addEventListener('click', () => {
          $.fancybox.open(projectImages, {
            loop: true,
            thumbs: {
              autoStart: true,
            },
            index: projectImages.indexOf(image),
          });
        });
        imageContainer.appendChild(thumbnail);
      });

      projectElement.appendChild(imageContainer);
      gameContainer.appendChild(projectElement);
    });
  }

  displayGame();